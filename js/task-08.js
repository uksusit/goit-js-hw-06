// Обробка відправлення форми form.login-form повинна відбуватися відповідно до події submit.
// Під час відправлення форми сторінка не повинна перезавантажуватися.
// Якщо у формі є незаповнені поля, виводь alert з попередженням про те, що всі поля
// повинні бути заповнені.
// Якщо користувач заповнив усі поля і відправив форму, збери значення полів в об'єкт,
//  де ім'я поля буде ім'ям властивості, а значення поля - значенням властивості.
// Для доступу до елементів форми використовуй властивість elements.
// Виведи об'єкт із введеними даними в консоль і очисти значення полів форми методом reset.

const loginForm = document.querySelector('.login-form');

loginForm.addEventListener('submit', onFormSubmit);

function onFormSubmit(event) {
  // 1. Зупиняємо перезавантаження сторінки
  event.preventDefault();

  // 2. Отримуємо доступ до елементів форми через властивість elements
  const {
    elements: { email, password }
  } = event.currentTarget;

  // 3. Перевірка на порожні поля
  if (email.value.trim() === "" || password.value.trim() === "") {
    return alert("Усі поля повинні бути заповнені!");
  }

  // 4. Збір даних в об'єкт
  const formData = {
    email: email.value,
    password: password.value,
  };

  // 5. Вивід об'єкта в консоль
  console.log(formData);

  // 6. Очищення форми
  event.currentTarget.reset();
}
// Деталі реалізації:
// event.preventDefault(): Це критично важливий метод. Стандартна поведінка 
// браузера при події submit — відправка даних на сервер і перезавантаження сторінки.
//  Ми це скасовуємо, щоб обробити дані скриптом.

// Деструктуризація elements: Код const { elements: { email, password } } = event.currentTarget 
// дозволяє швидко отримати посилання на інпути за їхніми іменами (name="email" та name="password").

// Метод trim(): Ми використовуємо його при перевірці, щоб користувач не міг 
// просто ввести кілька пробілів і "обманути" валідацію.

// Метод reset(): Найшвидший спосіб очистити всі поля форми до їхніх початкових 
// значень одним рядком.

